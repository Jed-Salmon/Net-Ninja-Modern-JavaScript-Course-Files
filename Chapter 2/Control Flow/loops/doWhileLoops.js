// do while loops

// An extension of a while loop. Runs at least once, even if the condition is not true.

let i = 4;

do {
  console.log("val of i is: ", i);
  i++;
} while (i < 5); // Add semicolon as it is no longer a code block {}.

// Runs once, returns 4 then adds 1 to make 5. Now 5 not less than 5 so it doesn't run the do while loop again.

// Summary: do while loops execute the block of code first, regardless of whether the condition is true or false
